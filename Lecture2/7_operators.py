# Operators 

# Assingment 
a = 2 
b = 3 

# Arithmetic 
print(a + b) 
print(a - b)
print(a * b)
print(a ** b) # a to power of b
print(a / b) # division that preserves decimal fractions 
print(a // b) # integer division - does not preserve decimal fractions
print(a % b) # modulus in python allows to use float values 

# Comparison 
print(a == b) # False 
print(a != b) # True
print(a > b)  # False
print(a >= b) # False
print(a < b)  # True
print(a <= b) # True
